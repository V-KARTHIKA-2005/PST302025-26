package pst;
public class AsciivalueAZ {
	    public static void main(String[] args) {
	        System.out.println("Uppercase Alphabets (A-Z) and their ASCII values:");
	        for (char ch = 'A'; ch <= 'Z'; ch++) {
	            int asciiValue = ch; 
	            System.out.println(ch + " = " + asciiValue);
	        }

	        }
	    }




